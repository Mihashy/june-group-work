    呼び出されたOmikuziAIがGeminiにおみくじ生成を依頼、返ってきた文章をリターン
    setFortuneを呼び出し、OmikuziAIからリターンされた文章をfortuneに突っ込む
    値の変更によってページが再レンダリング(App.tsxを再読み込み)
    FortuneDisplayが新たなfortune関数とともに再構築
